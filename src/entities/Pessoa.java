package entities;

public class Pessoa {

	// atributos (campos / dados)
	private Integer id;
	private String nome;

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getId() {
		return id;

	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getNome() {
		return nome;
	}

}
